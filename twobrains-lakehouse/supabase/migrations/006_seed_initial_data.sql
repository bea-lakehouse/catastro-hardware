begin
  select count(*) into v_c from silver.dim_client;
  select count(*) into v_e from silver.dim_employee;
  select count(*) into v_a from silver.dim_asset;
  select count(*) into v_m from silver.fact_movements;
  select count(*) into v_s from silver.fact_asset_snapshot;
  raise notice 'Seed verificado — clientes:% empleados:% activos:% movimientos:% snapshots:%',
    v_c, v_e, v_a, v_m, v_s;
  assert v_c  = 9,  'Error: dim_client count mismatch';
  assert v_e >= 60, 'Error: dim_employee too few';
  assert v_a >= 80, 'Error: dim_asset too few';
  assert v_m = 139, 'Error: fact_movements count mismatch';
  assert v_s >= 88, 'Error: fact_asset_snapshot too few';
  raise notice 'SEED OK — todas las afirmaciones pasaron';
end $$;