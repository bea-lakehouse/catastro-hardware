  rut_empleado         text,
  gestionado_por       text,

  -- Asset link
  serial               text        references dim_asset(serial) on delete set null,
  modelo_raw           text,
  cpu                  text,
  ram                  text,
  anio_fabricacion     smallint,
  precio_referencia_usd numeric(10,2),
  tiene_serial         boolean     not null default false,

  -- Business
  cliente              text,
  ubicacion            text,

  -- Enriched from dim_asset at ingest time
  asset_cpu            text,
  asset_anio           smallint,
  asset_estado_actual  asset_status,
  asset_risk_score     smallint,
  asset_risk_nivel     risk_level,
  asset_valor_dep      numeric(10,2),

  -- Quality flags
  fuente_hoja          text        not null,
  es_inferido          boolean     not null default false,

  -- ML target (to be filled by IT team)
  riesgo_percibido_it  risk_level,

  -- Traceability
  source_file          text,
  load_timestamp       timestamptz not null default now()
);

create index fact_movements_serial      on fact_movements(serial);
create index fact_movements_fecha       on fact_movements(fecha);
create index fact_movements_tipo        on fact_movements(tipo_movimiento);
create index fact_movements_cliente     on fact_movements(cliente);
create index fact_movements_fuente      on fact_movements(fuente_hoja);
