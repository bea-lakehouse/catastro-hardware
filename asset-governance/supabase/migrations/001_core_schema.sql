-- ── BRONZE — Raw ingestion log ────────────────────────────────
-- Every xlsx load produces one row per sheet processed.
-- Records are append-only and immutable.
create table bronze_ingestion_log (
  id               uuid        primary key default gen_random_uuid(),
  batch_id         uuid        not null,
  source_file      text        not null,
  source_sheet     text        not null,
  load_timestamp   timestamptz not null default now(),
  records_raw      integer     not null,
  status           ingestion_status not null default 'ok',
  error_message    text,
  metadata         jsonb       -- arbitrary key-value from the source
);

create index bronze_ingestion_log_batch_id  on bronze_ingestion_log(batch_id);
create index bronze_ingestion_log_timestamp on bronze_ingestion_log(load_timestamp desc);

-- ── SILVER — dim_asset ────────────────────────────────────────
-- Normalised master record for every physical asset.
-- Upsert on serial. Enriched by Silver pipeline from Bronze.
create table dim_asset (
  -- Identity
  serial               text        primary key,
  sku                  text,
  tipo                 text,
  marca                text,
  modelo               text,
  color                text,
  anio_fabricacion     smallint,

  -- Hardware
  cpu                  text,
  ram                  text,
  disco                text,
  pantalla             text,
  sistema_operativo    text,

  -- Estado
  estado               asset_status,
  condicion            asset_condition,

  -- Asignación
  empleado             text,
  rut_empleado         text,
  cliente              text,
  perfil               text,
  ambito_laboral       text,
  localizacion         text,
  ciudad               text,
  propiedad            text,

  -- Fechas
  fecha_compra         date,
  fecha_mantenimiento  date,
  fecha_asignacion     date,

  -- Batería
  ciclos_bateria       integer,
  condicion_bateria    text,

  -- Financiero (computed by Silver pipeline)
  valor_nuevo_usd      numeric(10,2),
  valor_dep_usd        numeric(10,2),
  dep_acumulada_usd    numeric(10,2),
  pct_depreciado       numeric(5,1),
  costo_renovacion_usd numeric(10,2),

  -- Risk & Quality (computed by Silver pipeline)
  risk_score           smallint    check (risk_score between 0 and 100),
  risk_nivel           risk_level,
  score_renovacion     smallint    check (score_renovacion between 0 and 100),
  candidato_renovacion boolean     not null default false,
  calidad_dato         smallint    check (calidad_dato between 0 and 100),

  -- Traceability (Bronze fields carried forward)
  fuente_hoja          text,
  source_file          text,
  load_timestamp       timestamptz not null default now(),
  updated_at           timestamptz not null default now()
);

create index dim_asset_estado   on dim_asset(estado);
create index dim_asset_cliente  on dim_asset(cliente);
create index dim_asset_riesgo   on dim_asset(risk_nivel);
create index dim_asset_updated  on dim_asset(updated_at desc);

-- Auto-update updated_at
create or replace function set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create trigger dim_asset_updated_at
  before update on dim_asset
  for each row execute function set_updated_at();

-- ── SILVER — fact_movements ───────────────────────────────────
-- Every discrete event on an asset. Append-only.
-- movement_id is generated by the Silver pipeline (MD5 of type+date+employee+serial).
create table fact_movements (
  movement_id          text        primary key,
  tipo_movimiento      movement_type not null,
  estado_resultante    asset_status,

  -- Temporal
  fecha                date,
